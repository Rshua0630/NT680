UINT32 *SenMode2TblFp_imx117_ff(UINT32 id, UINT32 *DzMaxidx)
{
	UINT32 *Ptr = NULL;
	UINT32 tbl_size,i;
	ER rt = E_OK;
	SEN_STATUS_PARAM sen_status = {0};
	UINT32 sen_mode;

	sen_mode = IPL_CtrlGetInfor(id, IPLCTRL_SEN_MODE);
	sen_status.sensor_mode = IPL_CtrlGetInfor(id, IPLCTRL_SEN_MODE);
	rt = sensor_get_info(IPL_UTI_CONV2_SEN_ID(IPL_CtrlGetInfor(id, IPLCTRL_MF_BASE_ID)), SEN_GET_STATUS, (void *)&sen_status);
	if (rt != E_OK) {
		DBG_ERR("Get Sensor Status Fail id:%d, Sensor_Mode: %d\r\n", IPL_CtrlGetInfor(id, IPLCTRL_SEN_MODE));
		return NULL;
	}

	UINT32 vdo_width = (IPL_AlgGetUIInfo(id, IPL_SEL_VIDEO_SIZE) & 0xffff0000) >> 16;
    UINT32 vdo_height = (IPL_AlgGetUIInfo(id, IPL_SEL_VIDEO_SIZE) & 0x0000ffff);
	UINT32 vdo_fps = (IPL_AlgGetUIInfo(id, IPL_SEL_VIDEO_FPS) & 0xffff0000) >> 16;

	//special case for manual select dzoom table
	switch (sen_mode) {
	case SENSOR_MODE_1:
	case SENSOR_MODE_2:
        if((vdo_width==2704)&&(vdo_height==2032)) {
    		Ptr = (UINT32 *)&VDOZOOM_INFOR_MODE_2_TABLE[0][0];
    		*DzMaxidx = (sizeof(VDOZOOM_INFOR_MODE_2_TABLE) / 4 / DZOOM_ITEM_MAX) - 1;
        } else {
    		Ptr = (UINT32 *)&VDOZOOM_INFOR_MODE_1_TABLE[0][0];
    		*DzMaxidx = (sizeof(VDOZOOM_INFOR_MODE_1_TABLE) / 4 / DZOOM_ITEM_MAX) - 1;
        }
		break;

	case SENSOR_MODE_3:
    	Ptr = (UINT32 *)&VDOZOOM_INFOR_MODE_3_TABLE[0][0];
		*DzMaxidx = (sizeof(VDOZOOM_INFOR_MODE_3_TABLE) / 4 / DZOOM_ITEM_MAX) - 1;
		break;

	case SENSOR_MODE_4:
	case SENSOR_MODE_7:
	case SENSOR_MODE_8:
		Ptr = (UINT32 *)&VDOZOOM_INFOR_MODE_4_TABLE[0][0];
		*DzMaxidx = (sizeof(VDOZOOM_INFOR_MODE_4_TABLE) / 4 / DZOOM_ITEM_MAX) - 1;
		break;

	case SENSOR_MODE_5:
		Ptr = (UINT32 *)&VDOZOOM_INFOR_MODE_5_TABLE[0][0];
		*DzMaxidx = (sizeof(VDOZOOM_INFOR_MODE_5_TABLE) / 4 / DZOOM_ITEM_MAX) - 1;
		break;

	case SENSOR_MODE_6:
        if(vdo_fps==240) {
    	    Ptr = (UINT32 *)&VDOZOOM_INFOR_MODE_6_240fps_TABLE[0][0];
    		*DzMaxidx = (sizeof(VDOZOOM_INFOR_MODE_6_240fps_TABLE) / 4 / DZOOM_ITEM_MAX) - 1;
        } else {
    	    Ptr = (UINT32 *)&VDOZOOM_INFOR_MODE_6_TABLE[0][0];
    		*DzMaxidx = (sizeof(VDOZOOM_INFOR_MODE_6_TABLE) / 4 / DZOOM_ITEM_MAX) - 1;
        }
		break;

	case SENSOR_MODE_9:
	    Ptr = (UINT32 *)&VDOZOOM_INFOR_MODE_9_TABLE[0][0];
		*DzMaxidx = (sizeof(VDOZOOM_INFOR_MODE_9_TABLE) / 4 / DZOOM_ITEM_MAX) - 1;
		break;

	case SENSOR_MODE_10:
	    Ptr = (UINT32 *)&VDOZOOM_INFOR_MODE_10_TABLE[0][0];
		*DzMaxidx = (sizeof(VDOZOOM_INFOR_MODE_10_TABLE) / 4 / DZOOM_ITEM_MAX) - 1;
		break;

	case SENSOR_MODE_11:
        if(vdo_fps==60) {
    	    Ptr = (UINT32 *)&VDOZOOM_INFOR_MODE_11_60fps_TABLE[0][0];
    		*DzMaxidx = (sizeof(VDOZOOM_INFOR_MODE_11_60fps_TABLE) / 4 / DZOOM_ITEM_MAX) - 1;
        } else {
    	    Ptr = (UINT32 *)&VDOZOOM_INFOR_MODE_11_TABLE[0][0];
    		*DzMaxidx = (sizeof(VDOZOOM_INFOR_MODE_11_TABLE) / 4 / DZOOM_ITEM_MAX) - 1;
        }
		break;

	default:
		Ptr = NULL;
		*DzMaxidx = 0;
		break;
	}

	tbl_size = sizeof(sensor_mode_dzoom_table) / sizeof(IPL_DZOOM_SEL_INFO);
	//auto gen dzoom table
	if(tbl_size == 0){
		//use default setting
		Ptr = ChkSenModeTbl(id, &sen_status.sensor_info, Ptr, DzMaxidx, NULL);
	} else {
		for(i=0;i<tbl_size;i++){
			if(sensor_mode_dzoom_table[i].mode == sen_mode){
				Ptr = ChkSenModeTbl(id, &sen_status.sensor_info, Ptr, DzMaxidx, &sensor_mode_dzoom_table[i]);
			} else {
				//use default setting
				Ptr = ChkSenModeTbl(id, &sen_status.sensor_info, Ptr, DzMaxidx, NULL);
			}
		}
	}
	return Ptr;
}
